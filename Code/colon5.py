def partition_dirichlet_fixed(df, n_clients, alpha=1.0, min_per_class=10,
                               min_size=50, seed=42, max_tries=200):
    rng = np.random.default_rng(seed)
    y = df["grade"].map(LABEL_MAP).to_numpy()
    n_classes = len(CLASS_NAMES)

    for attempt in range(max_tries):
        idx_by_class = [np.where(y == c)[0] for c in range(n_classes)]
        client_idx = [[] for _ in range(n_clients)]
        for c_idx in idx_by_class:
            rng.shuffle(c_idx)
            props = rng.dirichlet([alpha] * n_clients)
            splits = (np.cumsum(props) * len(c_idx)).astype(int)
            splits[-1] = len(c_idx)
            prev = 0
            for cli, end in enumerate(splits):
                client_idx[cli].extend(c_idx[prev:end].tolist())
                prev = end

        ok = True
        for ci in client_idx:
            if len(ci) < min_size:
                ok = False; break
            cdf_y = y[ci]
            for c in range(n_classes):
                if (cdf_y == c).sum() < min_per_class:
                    ok = False; break
            if not ok:
                break
        if ok:
            print(f"  Partition found on attempt {attempt+1}")
            break
    else:
        print(f"  WARNING: could not satisfy constraints after {max_tries} tries — using last attempt")

    return {i: df.iloc[ci].reset_index(drop=True) for i, ci in enumerate(client_idx)}


