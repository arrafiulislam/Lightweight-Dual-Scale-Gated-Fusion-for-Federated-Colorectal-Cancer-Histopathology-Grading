def run_fl_iid(
    splits, cfg: CFG, out_dir: Path, branch_name: str,
    fedprox_mu: float = 0.001,
    local_epochs: int = 2,
    max_rounds: int = 15,
    patience: int = 5,
):
    """Federated learning with IID partition and FedProx regularization.
    Runs for up to max_rounds; stops early if val F1 stops improving.
    """
    out_dir.mkdir(parents=True, exist_ok=True)
    print(f"\n{'='*60}")
    print(f"[FL-IID FedProx] {branch_name}  mu={fedprox_mu}  le={local_epochs}  "
          f"max_rounds={max_rounds}  clients={cfg.clients}")
    print(f"{'='*60}")

    client_dfs = partition_iid(splits["train"], cfg.clients, seed=cfg.seed)
