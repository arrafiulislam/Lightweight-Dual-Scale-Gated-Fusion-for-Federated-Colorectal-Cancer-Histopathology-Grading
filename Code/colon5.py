def make_transforms(size: int, train: bool = True):
    mean = (0.485, 0.456, 0.406)
    std  = (0.229, 0.224, 0.225)
    if train:
        try:
            rrc = A.RandomResizedCrop(size=(size, size), scale=(0.7, 1.0), ratio=(0.85, 1.15))
        except TypeError:
            rrc = A.RandomResizedCrop(height=size, width=size, scale=(0.7, 1.0), ratio=(0.85, 1.15))
        return A.Compose([
            rrc,
            A.HorizontalFlip(p=0.5),
            A.VerticalFlip(p=0.5),
            A.RandomRotate90(p=0.5),
            A.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.2, hue=0.1, p=0.6),
            A.HueSaturationValue(p=0.3),
            A.GaussianBlur(blur_limit=(3, 5), p=0.2),
            A.Normalize(mean=mean, std=std),
            ToTensorV2(),
        ])
    else:
        return A.Compose([
            A.LongestMaxSize(max_size=size),
            A.PadIfNeeded(min_height=size, min_width=size, border_mode=cv2.BORDER_CONSTANT),
            A.CenterCrop(height=size, width=size),
            A.Normalize(mean=mean, std=std),
            ToTensorV2(),
        ])


class CRCDataset(Dataset):
    def __init__(self, df: pd.DataFrame, tf_context, tf_detail):
        self.df = df.reset_index(drop=True).copy()
        self.tf_context = tf_context
        self.tf_detail  = tf_detail

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        img = cv2.imread(str(row["image_path"]))
        if img is None:
            raise RuntimeError(f"Cannot read: {row['image_path']}")
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        x_c = self.tf_context(image=img)["image"]
        x_d = self.tf_detail(image=img)["image"]
        y   = LABEL_MAP[row["grade"]]
        return (x_c, x_d), y


def make_sample_weights(df: pd.DataFrame):
    y = df["grade"].map(LABEL_MAP).to_numpy()
    counts = np.bincount(y, minlength=len(CLASS_NAMES)).astype(np.float64)
    inv = 1.0 / (counts + 1e-12)
    return torch.as_tensor(inv[y], dtype=torch.double)


def make_loader(df: pd.DataFrame, cfg: CFG, train: bool = True, use_sampler: bool = False):
    tf_c = make_transforms(cfg.size_context, train=train)
    tf_d = make_transforms(cfg.size_detail,  train=train)
    ds = CRCDataset(df, tf_c, tf_d)
    if train and use_sampler:
        weights = make_sample_weights(df)
        sampler = WeightedRandomSampler(weights, num_samples=len(weights), replacement=True)
        return DataLoader(ds, batch_size=cfg.batch_size, sampler=sampler,
                          shuffle=False, num_workers=cfg.num_workers,
                          pin_memory=(DEVICE.type=="cuda"), drop_last=True)
    return DataLoader(ds, batch_size=cfg.batch_size, shuffle=train,
                      num_workers=cfg.num_workers,
                      pin_memory=(DEVICE.type=="cuda"), drop_last=train)

