def build_index(data_root: Path, keep_4x: bool = True) -> pd.DataFrame:
    """Walk CRC-HGD-v2 directory and return a DataFrame of all images.
    Structure: grade_dir / mag_dir / image_file  (flat, no specimen subdir)
    """
    records = []
    for grade_dir, grade_label in GRADE_DIR_MAP.items():
        grade_path = data_root / grade_dir
        if not grade_path.exists():
            print(f"  WARNING: missing {grade_path}")
            continue
        for mag_dir in sorted(grade_path.iterdir()):
            if not mag_dir.is_dir():
                continue
            mag = mag_dir.name                    # e.g. "40x", "10x"
            mag_num = mag.rstrip('Xx').lstrip('0') or '0'
            if not keep_4x and mag_num == '4':
                continue
            for img_file in sorted(mag_dir.iterdir()):
                if img_file.suffix.lower() in IMAGE_EXTS:
                    parts = img_file.stem.split('_')
                    specimen_id = parts[3] if len(parts) > 3 else img_file.stem
                    records.append({
                        "image_path":    str(img_file),
                        "grade":         grade_label,
                        "magnification": mag,
                        "specimen_id":   specimen_id,
                    })
    df = pd.DataFrame(records)
    print(f"  Total images: {len(df)}")
    if df.empty:
        print("  WARNING: no images found — check DATA_ROOT")
        return df
    print(df['grade'].value_counts().to_string())
    print(df['magnification'].value_counts().to_string())
    return df


def split_by_image_stratified(df: pd.DataFrame, cfg: CFG):
    """Image-level stratified split into train / val / test."""
    df = df.sample(frac=1, random_state=cfg.seed).reset_index(drop=True)
    df_trainval, df_test = train_test_split(
        df, test_size=cfg.test_frac, stratify=df["grade"], random_state=cfg.seed
    )
    val_rel = cfg.val_frac / (1.0 - cfg.test_frac)
    df_train, df_val = train_test_split(
        df_trainval, test_size=val_rel, stratify=df_trainval["grade"], random_state=cfg.seed
    )
    return {
        "train": df_train.reset_index(drop=True),
        "val":   df_val.reset_index(drop=True),
        "test":  df_test.reset_index(drop=True),
    }

