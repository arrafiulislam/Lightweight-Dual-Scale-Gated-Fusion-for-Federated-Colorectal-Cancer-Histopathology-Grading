def partition_iid(df: pd.DataFrame, n_clients: int, seed: int = 42):
    """Stratified IID partition — equal class distribution per client."""
    skf = StratifiedKFold(n_splits=n_clients, shuffle=True, random_state=seed)
    clients = {}
    y = df["grade"].map(LABEL_MAP).to_numpy()
    for i, (_, idx) in enumerate(skf.split(df, y)):
        clients[i] = df.iloc[idx].reset_index(drop=True)
    return clients


def get_state_dict_cpu(model: nn.Module):
    return {k: v.detach().cpu().clone() for k, v in model.state_dict().items()}


def load_state_dict_cpu(model: nn.Module, sd: dict):
    model.load_state_dict({k: v.to(DEVICE) for k, v in sd.items()})


def fedavg_aggregate(client_sds: list, client_sizes: list):
    """Weighted FedAvg aggregation by client dataset size."""
    total = sum(client_sizes)
    agg = {}
    for key in client_sds[0]:
        agg[key] = sum(
            client_sds[i][key].float() * (client_sizes[i] / total)
            for i in range(len(client_sds))
        )
    return agg


def blend_sd(global_sd: dict, new_sd: dict, server_lr: float = 1.0):
    if server_lr == 1.0:
        return new_sd
    return {k: global_sd[k] + server_lr * (new_sd[k] - global_sd[k]) for k in global_sd}

