class SharedGatedDualScale(nn.Module):
    """Shared MobileNetV3 encoder with gated dual-scale feature fusion.

    Architecture:
        context branch (224) ─┐
                               ├─► gate ─► fused ─► head ─► logits
        detail  branch (320) ─┘
    The gate is a 2-layer MLP producing element-wise blend weights.
    """

    def __init__(self, backbone_name: str = BACKBONE, num_classes: int = 3, dropout: float = 0.3):
        super().__init__()
        self.encoder = timm.create_model(backbone_name, pretrained=True,
                                          num_classes=0, global_pool="avg")
        # Probe real output dim — timm num_features can differ from actual output
        with torch.no_grad():
            feat_dim = self.encoder(torch.zeros(1, 3, 224, 224)).shape[1]

        self.gate = nn.Sequential(
            nn.Linear(feat_dim * 2, feat_dim),
            nn.ReLU(inplace=True),
            nn.Linear(feat_dim, feat_dim),
            nn.Sigmoid(),
        )
        self.head = nn.Sequential(
            nn.LayerNorm(feat_dim),
            nn.Dropout(dropout),
            nn.Linear(feat_dim, num_classes),
        )

    def forward(self, x_context, x_detail):
        f_c = self.encoder(x_context)
        f_d = self.encoder(x_detail)
        g   = self.gate(torch.cat([f_c, f_d], dim=1))
        f   = g * f_d + (1.0 - g) * f_c
        return self.head(f)


def build_model(cfg: CFG) -> SharedGatedDualScale:
    return SharedGatedDualScale(BACKBONE, cfg.num_classes).to(DEVICE)


def count_params(model: nn.Module) -> int:
    return sum(p.numel() for p in model.parameters() if p.requires_grad)


