def run_centralized(splits, cfg: CFG, out_dir: Path, branch_name: str):
    out_dir.mkdir(parents=True, exist_ok=True)
    print(f"\n{'='*60}")
    print(f"[Centralized] {branch_name}  lr={cfg.lr}  epochs={cfg.central_epochs}")
    print(f"{'='*60}")

    train_loader = make_loader(splits["train"], cfg, train=True,  use_sampler=True)
    val_loader   = make_loader(splits["val"],   cfg, train=False)
    test_loader  = make_loader(splits["test"],  cfg, train=False)

    model     = build_model(cfg)
    criterion = make_criterion(splits["train"], cfg)
    optimizer = torch.optim.AdamW(model.parameters(), lr=cfg.lr, weight_decay=cfg.weight_decay)
    # Cosine annealing with warm restarts for better convergence
    scheduler = torch.optim.lr_scheduler.CosineAnnealingWarmRestarts(
        optimizer, T_0=10, T_mult=1, eta_min=1e-6
    )
    scaler = torch.cuda.amp.GradScaler() if cfg.use_amp else None

    best_val_f1, best_sd, history = -1.0, None, []
    patience_counter, patience_limit = 0, 7   # early stopping

    for epoch in range(1, cfg.central_epochs + 1):
        t_loss, t_acc = train_one_epoch(model, train_loader, optimizer, criterion, scaler)
        v_metrics     = evaluate(model, val_loader)
        scheduler.step()

        row = {
            "epoch":      epoch,
            "train_loss": round(t_loss,                   4),
            "train_acc":  round(t_acc,                    4),
            "val_acc":    round(v_metrics["acc"],         4),
            "val_f1":     round(v_metrics["macro_f1"],    4),
            "val_auc":    round(v_metrics["macro_auc"],   4),
        }
        history.append(row)
        print(f"  Epoch {epoch:2d}/{cfg.central_epochs}  "
              f"loss={t_loss:.4f}  t_acc={t_acc:.3f}  "
              f"val_acc={v_metrics['acc']:.3f}  val_f1={v_metrics['macro_f1']:.3f}  "
              f"val_auc={v_metrics['macro_auc']:.3f}")

        if v_metrics["macro_f1"] > best_val_f1:
            best_val_f1 = v_metrics["macro_f1"]
            best_sd     = get_state_dict_cpu(model)
            patience_counter = 0
        else:
            patience_counter += 1
            if patience_counter >= patience_limit:
                print(f"  Early stopping at epoch {epoch} (patience={patience_limit})")
                break

    load_state_dict_cpu(model, best_sd)
    test_metrics = evaluate(model, test_loader)

    print(f"\n  [TEST] acc={test_metrics['acc']:.4f}  "
          f"f1={test_metrics['macro_f1']:.4f}  "
          f"auc={test_metrics['macro_auc']:.4f}  "
          f"recalls={[round(r,3) for r in test_metrics['recall_per_class']]}")

    # Save outputs
    hist_df = pd.DataFrame(history)
    hist_df.to_csv(out_dir / "central_history.csv", index=False)
    save_metrics(test_metrics, out_dir / "central_test_metrics.csv")
    torch.save(best_sd, out_dir / "central_best_model.pt")
    plot_confusion_matrix(
        test_metrics["cm"], CLASS_NAMES,
        f"Centralized — {branch_name}",
        out_dir / "central_confusion_matrix.png"
    )
    plot_convergence(history, f"Centralized — {branch_name}",
                     out_dir / "central_convergence.png")
    print(f"  Saved outputs to {out_dir}")
    return test_metrics

print("Centralized runner defined.")

