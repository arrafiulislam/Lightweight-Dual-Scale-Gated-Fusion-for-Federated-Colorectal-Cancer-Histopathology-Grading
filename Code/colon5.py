def plot_confusion_matrix(cm, class_names, title, out_path: Path):
    """Normalized + raw count confusion matrix for publication."""
    cm_norm = cm.astype(float) / (cm.sum(axis=1, keepdims=True) + 1e-12)
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    for ax, data, fmt, ttl in zip(
        axes,
        [cm, cm_norm],
        ['d', '.2f'],
        ['Raw Counts', 'Normalized (Recall)'],
    ):
        sns.heatmap(data, annot=True, fmt=fmt, cmap='Blues',
                    xticklabels=class_names, yticklabels=class_names,
                    ax=ax, vmin=0, vmax=(1 if fmt == '.2f' else None),
                    linewidths=0.5, linecolor='lightgray')
        ax.set_xlabel('Predicted Label', fontsize=11)
        ax.set_ylabel('True Label', fontsize=11)
        ax.set_title(f'{title}\n{ttl}', fontsize=11)
        ax.set_xticklabels(ax.get_xticklabels(), rotation=30, ha='right')
