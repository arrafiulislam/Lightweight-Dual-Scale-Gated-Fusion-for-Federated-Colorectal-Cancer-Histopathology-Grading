def make_criterion(df: pd.DataFrame, cfg: CFG):
    """Inverse-frequency weighted CrossEntropyLoss with label smoothing."""
    y = df["grade"].map(LABEL_MAP).to_numpy()
    counts = np.bincount(y, minlength=len(CLASS_NAMES)).astype(np.float32)
    w = 1.0 / (counts + 1e-6)
    w = w / w.sum() * len(CLASS_NAMES)
    weight = torch.tensor(w, dtype=torch.float32, device=DEVICE)
    return nn.CrossEntropyLoss(weight=weight, label_smoothing=cfg.label_smoothing)


def train_one_epoch(
    model, loader, optimizer, criterion,
    scaler=None, global_params=None, mu: float = 0.0
):
    model.train()
    total_loss, correct, n = 0.0, 0, 0
    for (x_c, x_d), y in loader:
        x_c, x_d, y = x_c.to(DEVICE), x_d.to(DEVICE), y.to(DEVICE)
        optimizer.zero_grad()
        if scaler is not None:
            with torch.autocast(device_type="cuda"):
                logits = model(x_c, x_d)
                loss   = criterion(logits, y)
                if global_params and mu > 0:
                    prox = sum(
                        ((p - gp.to(DEVICE)) ** 2).sum()
                        for (_, p), gp in zip(model.named_parameters(), global_params)
                        if p.requires_grad
                    )
                    loss = loss + (mu / 2.0) * prox
            scaler.scale(loss).backward()
            scaler.step(optimizer)
            scaler.update()
        else:
            logits = model(x_c, x_d)
            loss   = criterion(logits, y)
            if global_params and mu > 0:
                prox = sum(
                    ((p - gp.to(DEVICE)) ** 2).sum()
                    for (_, p), gp in zip(model.named_parameters(), global_params)
                    if p.requires_grad
                )
                loss = loss + (mu / 2.0) * prox
            loss.backward()
            optimizer.step()
        total_loss += loss.item() * len(y)
        correct    += (logits.argmax(1) == y).sum().item()
        n          += len(y)
    return total_loss / n, correct / n


@torch.no_grad()
def evaluate(model, loader):
    model.eval()
    all_preds, all_labels, all_probs = [], [], []
    for (x_c, x_d), y in loader:
        x_c, x_d = x_c.to(DEVICE), x_d.to(DEVICE)
        logits = model(x_c, x_d)
        probs  = torch.softmax(logits, dim=1).cpu().numpy()
        preds  = logits.argmax(1).cpu().numpy()
        all_preds.extend(preds)
        all_labels.extend(y.numpy())
        all_probs.extend(probs)
    acc = accuracy_score(all_labels, all_preds)
    f1  = f1_score(all_labels, all_preds, average="macro", zero_division=0)
    cm  = confusion_matrix(all_labels, all_preds, labels=list(range(len(CLASS_NAMES))))
    per_class_recall = cm.diagonal() / (cm.sum(axis=1) + 1e-12)
    per_class_prec   = cm.diagonal() / (cm.sum(axis=0) + 1e-12)
    per_class_f1     = 2 * per_class_recall * per_class_prec / (per_class_recall + per_class_prec + 1e-12)
    try:
        auc = roc_auc_score(all_labels, np.array(all_probs), multi_class='ovr', average='macro')
    except Exception:
        auc = float('nan')
    return {
        "acc": acc, "macro_f1": f1, "macro_auc": auc,
        "recall_per_class":    per_class_recall.tolist(),
        "precision_per_class": per_class_prec.tolist(),
        "f1_per_class":        per_class_f1.tolist(),
        "cm": cm,
        "preds": all_preds, "labels": all_labels, "probs": np.array(all_probs),
    }

